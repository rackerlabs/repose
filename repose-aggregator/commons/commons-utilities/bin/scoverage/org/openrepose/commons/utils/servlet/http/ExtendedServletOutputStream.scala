package org.openrepose.commons.utils.servlet.http

import java.io.InputStream
import javax.servlet.ServletOutputStream

trait ExtendedServletOutputStream extends ServletOutputStream {
  /**
   * Returns an [[InputStream]] which contains, in a readable form, all of the data written to this object.
   */
  def getOutputStreamAsInputStream: InputStream

  /**
   * Sets the content of this object.
   *
   * @param in the [[InputStream]] which will be read and written to this object.
   */
  def setOutput(in: InputStream): Unit

  /**
   * Applies any un-applied changes. Specifically, writes data from this object into a wrapped OutputStream.
   */
  def commit(): Unit

  /**
   * Clears any internal buffers maintained by this OutputStream.
   */
  def resetBuffer(): Unit
}
