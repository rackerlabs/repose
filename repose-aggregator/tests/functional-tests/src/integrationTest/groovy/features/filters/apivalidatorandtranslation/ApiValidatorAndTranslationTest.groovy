package features.filters.apivalidatorandtranslation

import org.junit.experimental.categories.Category
import org.openrepose.framework.test.ReposeValveTest
import org.rackspace.deproxy.Deproxy
import org.rackspace.deproxy.MessageChain
import scaffold.category.XmlParsing

/**
 * Testing that the Translation filter and API validator filter work in the same filter chain
 * given that they both rely on Saxon, but are in separate EAR artifacts.
 */
@Category(XmlParsing)
class ApiValidatorAndTranslationTest extends ReposeValveTest {

    def setupSpec() {
        deproxy = new Deproxy()
        deproxy.addEndpoint(properties.targetPort)

        def params = properties.getDefaultTemplateParams()
        repose.configurationProvider.applyConfigs("common", params)
        repose.configurationProvider.applyConfigs("features/filters/apivalidatorandtranslation", params)
        repose.start()
        repose.waitForNon500FromUrl(reposeEndpoint)
    }

    def "a valid request should make it to the origin service"() {
        when:
        MessageChain messageChain = deproxy.makeRequest(url: reposeEndpoint + "/resource",
                method: "POST",
                headers: ["Content-Type": "application/xml"],
                requestBody: """
                    <root>
                        text-value
                    </root>
                """)

        then:
        messageChain.receivedResponse.code == "200"
    }
}
